    private void jButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualActionPerformed
        // TODO add your handling code here:
        //Aqui vamos a hacer las cuentas
        
        //Vamos a pillar lo que haya y hacemos un rejunto to wapo :D
        boolean cont = false;
        JTextField textField = getjScreen();
        String text = getjScreen().getText();
        if(text.charAt(text.length()-1) != '-' && text.charAt(text.length()-1) != '+' && text.charAt(text.length()-1) != '*' && text.charAt(text.length()-1) != '/' && text.charAt(text.length()-1) != '.'){
            cont = true;
        }
        if((text.contains("-") || text.contains("+") || text.contains("*") || text.contains("/")) && cont){
          ScriptEngineManager mgr = new ScriptEngineManager();
          ScriptEngine engine = mgr.getEngineByName("JavaScript");
            try {
                textField.setText(String.valueOf(engine.eval(text)));
            } catch (ScriptException ex) {
                Logger.getLogger(Calculator.class.getName()).log(Level.SEVERE, null, ex);
            }
        clear = true;
        }else{
            //No hay nada que calcular
            System.out.println("-Err");
        }
        
    }//GEN-LAST:event_jButtonEqualActionPerformed


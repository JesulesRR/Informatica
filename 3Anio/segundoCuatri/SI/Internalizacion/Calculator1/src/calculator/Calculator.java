    private void jButtonNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNumberActionPerformed
        // TODO add your handling code here:
        if(clear){
            //Limpiamos despues de una operacion
            JTextField text = getjScreen();
            text.setText("");
            clear = false;
            firstTimeMinus = true;
        }
        calculatorText((JButton) evt.getSource());
        signalFlag = true;
        commaFlag = true;
        
    }//GEN-LAST:event_jButtonNumberActionPerformed

    private void jButtonCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCActionPerformed
        // TODO add your handling code here:
        JTextField text = getjScreen();
        char lastLetter = text.getText().charAt(text.getText().length()-1);
        if(lastLetter == '+' || lastLetter == '-' || lastLetter == '*' || lastLetter == '/'){
            signalFlag = true;
        }
        if(lastLetter == '.'){
            commaFlag = true;
            watchingComma = false;
        }
        text.setText(text.getText().substring(0, text.getText().length() - 1));
        
    }//GEN-LAST:event_jButtonCActionPerformed

    private void jButtonPointActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPointActionPerformed
        // TODO add your handling code here:
        if(commaFlag && !watchingComma){
            calculatorText((JButton) evt.getSource());
            commaFlag = false;
            watchingComma = true;
        }
    }//GEN-LAST:event_jButtonPointActionPerformed

    
